        super().__init__(health)
        self.max_health = health
        self.character_class = character_class
        self.player_name = player_name
        self.row_pos = 3
        self.col_pos = 3
        self.dodge_chance = dodge_chance
        self.weapon = None
        self.gold = 0
        self.inventory = {"Health Potion": 2}
        self.is_hero = False
        self.combat_streak = False
        
        
