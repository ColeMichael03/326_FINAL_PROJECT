            if move_direction.lower() not in ["w", "a", "s", "d"]:
                print("Invalid direction!")
            
            
            #FORWARD
            if move_direction == "w":
                #If at top wall already, cannot move forwards
                if (self.row_pos == 0):
        
                    print("you slam your face into a wall... "
                        "it seems this is as far forward as you can go.")
                    
